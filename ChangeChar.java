
public class ChangeChar {
  public static void main(String[] args) {
    System.out.println("北京\t上海\t广州");
    System.out.println("你好\n世界");
    System.out.println("hello\\world");
    System.out.println("C:\\usr\\locale\\bin");
    System.out.println("C:\\\\usr\\\\locale\\\\bin");
    System.out.println("老师说: \"今天体育课不上了\"");
    System.out.println("老师说: \'今天体育课不上了\'");
    System.out.println("我被替换了啊: \r世界");
    System.out.println("你好呀: \r\n世界");
  }
}
